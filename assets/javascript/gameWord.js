var guessedLetters = [];
var guessingWord = [];
var wordToMatch;
var numGuess;
var wins = 0;
var pause = false;








function initializeGame() {

  // 2 lines : 1st frees up space and 2nd hides image
  document.getElementById("imgToShow").style.position="absolute";
  document.getElementById("imgToShow").style.visibility="hidden";

  let motive = motiveMsg[Math.floor(Math.random() * motiveMsg.length)];

  document.getElementById('welcome').className = 'no-blink';
  document.getElementById('welcome').innerHTML = motive;


  wordToMatch = wordsList[Math.floor(Math.random() * wordsList.length)].toUpperCase();


  var wordlen = wordToMatch.length;
  var coverup = 1;
  if (wordlen >= 6) {
    coverup = wordlen - 3;
  }

  if (coverup > 4) {
    numGuess = 3;
  }
  else {
    numGuess = 1;
  }

  let runder = [];
  while (runder.length != coverup) {
    let rn = Math.floor(Math.random() * wordlen) + 1;
    if (rn == 0 || rn == 1 || rn >= wordlen) { continue; }
    if (runder.includes(rn)) { continue; }
    else { runder.push(rn) };
  }


  for (var i = 0; i < wordToMatch.length; i++) {

    if (wordToMatch[i] === " ") {
      guessingWord.push("  ")
    }
    else if (runder.includes(i + 1)) {
      guessingWord.push("_");
    }
    else {
      guessingWord.push(wordToMatch[i]);
    }
  }
  updateDisplay();
};








